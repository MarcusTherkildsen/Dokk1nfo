            <div class="col-xs-12 col-xs-offset-0">
                <h1 id="hvordan">Hvordan virker det?</h1>
                <hr class="slim-margin">
                <p>
                    Den indtastede søgning bliver sendt til <a href="https://www.aakb.dk/">aakb.dk</a> hvorefter søgeresultaternes unikke id, kaldet FAUST-id, bliver scrapet. Med det scrapes der for yderligere detaljer, såsom placering og forfatter. Til sidst bruges placeringen til at søge i Dokk1nfos hylde-database, hvorefter det hele samles i en letoverskuelig oversigt. 
                </p>
            </div>
        </div>

        <div class="row">
            <div class="col-xs-12 col-xs-offset-0">
                <h1 id="spoergsmaal">Hvorfor kan jeg ikke finde X? Hvorfor flere hylder?</h1>
                <hr class="slim-margin">
                <p>
                    Da Dokk1nfo er lavet i min fritid (dvs. helt gratis for alle andre end mig selv) er siden ikke perfekt. Specielt mangles der stadig ordentlig DVD og børnesøgning, hvilket vil komme med tiden. Hvis du støder på en bestemt søgning som du mener flere vil få gavn af virker, så send endelig <a href="mailto:marcus@mtherkildsen.dk">en mail</a> (marcus@mtherkildsen.dk) til mig og så kigger jeg på det.<br>
                    Hvis alt glipper, så kan du forsøge dig med den nuværende løsning: <a href="https://www.aakb.dk/">aakb.dk</a>.
                </p>
                <p>
                    Nogen søgninger vil give flere mulige hylder. F.eks. er der tre hylder, der alle indeholder biografier og derfor har samme nummer, 99.4. Når en bog matcher det nummer vises der derfor 3 hylder.  
                </p>
