                <h1 id="om">Om Dokk1nfo.dk</h1>
                <hr class="slim-margin">
                <p>
                    Dokk1nfo.dk er lavet af <a href="http://mtherkildsen.dk">Marcus Therkildsen</a> fordi jeg blev træt af ikke at kunne finde rundt på det største offentlige bibliotek i Skandinavien, <a href="https://da.wikipedia.org/wiki/Dokk1">Dokk1</a>. Der har indtil nu manglet et ordentligt system til at finde det man søger, så jeg har ofte oplevet at gå helt forkert. Det er slut nu.
                </p>
